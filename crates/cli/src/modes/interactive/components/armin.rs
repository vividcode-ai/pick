const BYTES_PER_ROW: usize = 4;
const DISPLAY_HEIGHT: usize = HEIGHT / 2;

fn get_pixel(x: usize, y: usize) -> bool {
    if y >= HEIGHT { return false; }
    let byte_index = y * BYTES_PER_ROW + x / 8;
    let bit_index = x % 8;
    if byte_index >= BITS.len() { return false; }
    ((BITS[byte_index] >> bit_index) & 1) == 0
}

fn get_char(x: usize, row: usize) -> char {
    let upper = get_pixel(x, row * 2);
    let lower = get_pixel(x, row * 2 + 1);
    if upper && lower { '█' }
    else if upper { '▀' }
    else if lower { '▄' }
    else { ' ' }
}

/// Render the Armin ASCII art
pub fn render_armin(width: usize) -> Vec<String> {
    let mut lines = Vec::new();
    let padding = 1;
    let available = width.saturating_sub(padding);

    for row in 0..DISPLAY_HEIGHT {
        let mut line = String::new();
        for x in 0..WIDTH {
            line.push(get_char(x, row));
        }
        let clipped = if line.len() > available {
            &line[..available]
        } else {
            &line
        };
        let pad = " ".repeat(width.saturating_sub(padding + clipped.len()));
        lines.push(format!(" {}{}", ToolTheme::fg("accent", clipped), pad));
    }

    let msg = "ARMIN SAYS HI";
    let msg_pad = " ".repeat(width.saturating_sub(padding + msg.len()));
    lines.push(format!(" {}{}", ToolTheme::fg("accent", msg), msg_pad));

    lines
}
